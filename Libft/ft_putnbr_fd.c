#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	long	a;
	char	c;

	a = n;
	if (a < 0)
	{
		write(fd, "-", 1);
		a = -a;
	}
	if (a > 9)
		ft_putnbr_fd((a / 10), fd);
	c = (a % 10) + '0';
	(void)write(fd, &c, 1);
}
